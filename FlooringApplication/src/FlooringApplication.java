    private void calculateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateButtonActionPerformed
        double width, length, area, cost, unit;
        try {
            width = Double.parseDouble(floorWidthTextField.getText());
            length = Double.parseDouble(floorLengthTextField.getText());
            area = width * length;
            unit = woodRadioButton.isSelected() ? 30 : 15;
            cost = unit * area;
            floorAreaTextField.setText(area + " ft2");
            floorCostTextField.setText("$" + cost);
        } catch (Exception e) {
            floorAreaTextField.setText("Please enter valid width of floor and length)");
            floorCostTextField.setText(e.toString());
        }
    }//GEN-LAST:event_calculateButtonActionPerformed

    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        StringBuilder sb = new StringBuilder();
        sb.append("Customer Name: ").append(customerNameTextField.getText()).append("\n");
        sb.append("Customer Address: ").append(customerAddressTextField.getText()).append("\n");
        sb.append("Floor Width: ").append(floorWidthTextField.getText()).append("\n");
        sb.append("Floor Length: ").append(floorLengthTextField.getText()).append("\n");
        String type = woodRadioButton.isSelected() ? "Wood" : "Carpet";
        sb.append("Floor Type: ").append(type).append("\n");
        sb.append("Floor Area: ").append(floorAreaTextField.getText()).append("\n");
        sb.append("Floor Cost: ").append(floorCostTextField.getText()).append("\n");
        summaryTextArea.setText(sb.toString());

        double width, length, area, cost, unit;
        cost = 0;
        area = 0;
        try {
            width = Double.parseDouble(floorWidthTextField.getText());
            length = Double.parseDouble(floorLengthTextField.getText());
            area = width * length;
            unit = woodRadioButton.isSelected() ? 30 : 15;
            cost = unit * area;
        } catch (Exception e) {
        }
        list.add(new Row(list.size() + 1, customerNameTextField.getText(), customerAddressTextField.getText(), type, area, cost));
        writeToSQL(customerNameTextField.getText(), customerAddressTextField.getText(), type, area, cost);
    }//GEN-LAST:event_submitButtonActionPerformed

    private void orderListButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderListButtonActionPerformed
        int index = 0;
        for (Row row : list) {
            orderListTable.setValueAt(row.id, index, 0);
            orderListTable.setValueAt(row.name, index, 1);
            orderListTable.setValueAt(row.address, index, 2);
            orderListTable.setValueAt(row.type, index, 3);
            orderListTable.setValueAt(row.area + " ft2", index, 4);
            orderListTable.setValueAt("$" + row.cost, index, 5);
            index++;
        }
    }//GEN-LAST:event_orderListButtonActionPerformed

